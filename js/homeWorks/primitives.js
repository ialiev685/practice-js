console.log('---primitives---');

// string
let stringData = 'string data';
console.log(stringData);
console.log(typeof stringData);

stringData = ' ';
console.log(stringData);
console.log(typeof stringData);

stringData = '';
console.log(stringData);
console.log(typeof stringData);

// number
let number = 5;
console.log(number);
console.log(typeof number);

number = 2e56;
console.log(number);
console.log(typeof number);

number = Infinity;
console.log(number);
console.log(typeof number);

number = NaN;
console.log(number);
console.log(typeof number);

// boolean true || false
let bool = true;
console.log(bool);
console.log(typeof bool);

bool = false;
console.log(bool);
console.log(typeof bool);

//undefined
let message;
console.log(message);
console.log(typeof message);

// null
let user = null;
console.log(user);
console.log(typeof user);
